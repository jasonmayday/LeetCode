
a = [1,2,3,4,5]
print(a)


print(a[-1])        # 取最后一个元素。      
# 5
 
print(a[:-1])       # 除了最后一个取全部    
# [1, 2, 3, 4]

print(a[1:])        # 取第二个到最后一个元素
# [2, 3, 4, 5]

print(a[::-1])      # 取从后向前（相反）的元素
# [5, 4, 3, 2, 1]
 
print(a[2::-1])     # 取从下标为2的元素翻转读取
# [3, 2, 1]

""" 翻转第 i 位之后的元素"""
res = [1,2,3,4,5]
i = 2
res[i:] = res[:i-1:-1]
print (res)
# [1, 2, 5, 4, 3]